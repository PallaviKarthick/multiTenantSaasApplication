CREATE TABLE IF NOT EXISTS `TENANT_D` (
  `USER_ID` INT NOT NULL,
  `USER_NAME` VARCHAR(45) NOT NULL,
  `COMMENTS` VARCHAR(50) NULL)
ENGINE = InnoDB;


CREATE TABLE IF NOT EXISTS `TENANT_TABLE` (
  `TENANT_ID` VARCHAR(10) NOT NULL,
  `TABLE_NAME` VARCHAR(45) NOT NULL,
  `TABLE_DESC` VARCHAR(80) NULL,
  PRIMARY KEY (`TENANT_ID`, `TABLE_NAME`))
ENGINE = InnoDB;


CREATE TABLE IF NOT EXISTS `TENANT_FIELDS` (
  `TENANT_ID` VARCHAR(10) NOT NULL,
  `TABLE_NAME` VARCHAR(45) NOT NULL,
  `FIELD_NAME` VARCHAR(45) NOT NULL,
  `FIELD_TYPE` VARCHAR(80) NULL,
  `FIELD_COLUMN` INT NOT NULL,
  PRIMARY KEY (`TENANT_ID`, `TABLE_NAME`, `FIELD_NAME`))
  ENGINE = InnoDB;


CREATE TABLE IF NOT EXISTS `TENANT_DATA` (
  `RECORD_ID` INT NOT NULL AUTO_INCREMENT=1000 ,
  `TENANT_ID` VARCHAR(10) NOT NULL,
  `TENANT_TABLE` VARCHAR(45) NULL,
  `STUDENT_ID`  VARCHAR(45) NULL,
  `STUDENT_NAME`  VARCHAR(45) NULL,
  `GRADER_NAME` VARCHAR(45) NULL,
  `COLUMN_1` VARCHAR(80) NULL,
  `COLUMN_2` VARCHAR(80) NULL,
  `COLUMN_3` VARCHAR(80) NULL,
  `COLUMN_4` VARCHAR(80) NULL,
  PRIMARY KEY (`RECORD_ID`, `TENANT_ID`));

