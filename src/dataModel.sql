CREATE TABLE IF NOT EXISTS `TENANT_A` (
  `USER_ID` INT NOT NULL,
  `USER_NAME` VARCHAR(45) NOT NULL,
  `POINTS` INT NOT NULL,
  `SCALE`  INT NOT NULL,
  `COMPLETE` BOOLEAN NOT NULL,
  `COMMENTS` VARCHAR(50) NULL)
ENGINE = InnoDB;


CREATE TABLE IF NOT EXISTS `TENANT_B` (
  `USER_ID` INT NOT NULL,
  `USER_NAME` VARCHAR(45) NOT NULL,
  `POINTS` INT NOT NULL,
